  selectType(e) {
    const type = e.currentTarget.dataset.type
    this.setData({ selectedType: type })
    this.filterVaccines()
  },

  filterVaccines() {
    const { vaccines, selectedType } = this.data
    let filtered = selectedType === 'all'
      ? vaccines
      : vaccines.filter(v => v.type === selectedType)
    
    // 按接种时间排序
    filtered.sort((a, b) => {
      const timeA = this.getSortTime(a.time)
      const timeB = this.getSortTime(b.time)
      return timeA - timeB
    })
    
    this.setData({ filteredVaccines: filtered })
  },

  getSortTime(time) {
    if (!time) return 999
    
    const numMatch = time.match(/(\d+)/)
    if (!numMatch) return 999
    
    const num = parseInt(numMatch[1])
    
    if (time.includes('日龄')) return num
    if (time.includes('天')) return num + 100
    if (time.includes('周')) return num * 7 + 200
    if (time.includes('月')) return num * 30 + 300
    if (time.includes('每')) return num + 500
    if (time.includes('配种前')) return 400
    if (time.includes('产前')) return 350
    if (time.includes('妊娠')) return 300
    if (time.includes('断奶')) return 28
    if (time.includes('蚊虫')) return 365
    
    return 999
  },

